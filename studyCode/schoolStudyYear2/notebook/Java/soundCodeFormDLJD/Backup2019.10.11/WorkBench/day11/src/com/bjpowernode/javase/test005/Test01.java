package com.bjpowernode.javase.test005;

/**
 * 访问控制权限修饰符
 *
 *		1.访问控制权限修饰符来控制元素的访问范围
 *
 *		2.访问控制权限修饰符包括：
 *				
 *				public									表示公开的，任何位置都能访问
 *				protected                             同包，子类
 *				缺省(default)							同包
 *				private                                  私有的，只能在本类中访问
 *
 *		3.访问控制权限修饰符可以修饰类、方法、变量...
 *
 *		4.当某个数据只能希望子类使用，使用protected进行修饰
 *
 *		5.修饰符范围：
 *			
 *			private < 缺省 < protected < public 
 *
 *		6.类只能采用public和缺省来进行修饰，内部类除外
 *
 */
public class Test01 {

	public static void main(String[] args) {

		//创建User对象，访问i和j
		User u=new User();
		
		System.out.println(u.i);
		
		System.out.println(u.j);
		
	}

}
